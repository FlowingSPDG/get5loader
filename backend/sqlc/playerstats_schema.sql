CREATE TABLE `player_stats` (
  `id` VARCHAR(36) NOT NULL PRIMARY KEY AUTO_INCREMENT,
  `match_id` VARCHAR(36) NOT NULL,
  `map_id` VARCHAR(36) NOT NULL,
  `team_id` VARCHAR(36) NOT NULL,
  `steam_id` BIGINT UNSIGNED NOT NULL,
  `name` varchar(40) NOT NULL,
  `kills` SMALLINT NOT NULL,
  `deaths` int(11) NOT NULL,
  `roundsplayed` TINYINT UNSIGNED NOT NULL,
  `assists` TINYINT NOT NULL,
  `flashbang_assists` TINYINT UNSIGNED NOT NULL,
  `teamkills` TINYINT UNSIGNED NOT NULL,
  `suicides` TINYINT UNSIGNED NOT NULL,
  `headshot_kills` TINYINT UNSIGNED NOT NULL,
  `damage` MEDIUMINT UNSIGNED NOT NULL,
  `bomb_plants` TINYINT UNSIGNED NOT NULL,
  `bomb_defuses` TINYINT UNSIGNED NOT NULL,
  `v1` TINYINT UNSIGNED NOT NULL,
  `v2` TINYINT UNSIGNED NOT NULL,
  `v3` TINYINT UNSIGNED NOT NULL,
  `v4` TINYINT UNSIGNED NOT NULL,
  `v5` TINYINT UNSIGNED NOT NULL,
  `k1` TINYINT UNSIGNED NOT NULL,
  `k2` TINYINT UNSIGNED NOT NULL,
  `k3` TINYINT UNSIGNED NOT NULL,
  `k4` TINYINT UNSIGNED NOT NULL,
  `k5` TINYINT UNSIGNED NOT NULL,
  `firstdeath_Ct` TINYINT UNSIGNED NOT NULL,
  `firstdeath_t` TINYINT UNSIGNED NOT NULL,
  `firstkill_ct` TINYINT UNSIGNED NOT NULL,
  `firstkill_t` TINYINT UNSIGNED NOT NULL,
  FOREIGN KEY (`match_id`) REFERENCES matches(`id`),
  FOREIGN KEY (`map_id`) REFERENCES map_stats(`id`),
  FOREIGN KEY (`team_id`) REFERENCES teams(`id`),
  UNIQUE KEY `unique_player_stats` (`match_id`, `map_id`, `team_id`, `steam_id`)
);